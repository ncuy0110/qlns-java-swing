        private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
		try {
			JTable source = (JTable) evt.getSource();
			int row = source.rowAtPoint(evt.getPoint());
			EmployeeLog el = eltm.getRow(row);
			eltm.setSelectedRow(el);

			reloadCbb(el.getEmployee());
			taReason.setText(el.getReason());
			bgReward.clearSelection();
			lbID.setText("ID: " + el.getId());
			if (el.getReward()) {
				rbYes.setSelected(true);
			} else {
				rbNo.setSelected(true);
			}
			tfAmount.setText(el.getAmount() + "");
		} catch (Exception e) {

		}
        }//GEN-LAST:event_tableMouseClicked

        private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
		try {
			EmployeeLog el = eltm.getSelectedRow();
			String reason = taReason.getText();
			Employee e = (Employee) cbEmployee.getSelectedItem();
			Boolean reward = rbYes.isSelected();
			Long amount = Long.parseLong(tfAmount.getText());
			EmployeeLog elUpdate = new EmployeeLog(e, reward, amount, reason);
			elUpdate.setId(el.getId());
			if (EmployeeLogDao.update(elUpdate)) {
				JOptionPane.showMessageDialog(null, "Cập nhật thành công");
				reloadTable();
				return;
			}
			JOptionPane.showMessageDialog(null, "Cập nhật thất bại");

		} catch (Exception e) {
			JOptionPane.showMessageDialog(null, "Vui lòng chọn một hàng và điền thông tin hợp lệ!");
		}
        }//GEN-LAST:event_btnUpdateActionPerformed

        private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
		try {
			EmployeeLog el = eltm.getSelectedRow();
			if (EmployeeLogDao.delete(el.getId())) {
				JOptionPane.showMessageDialog(null, "Xóa thành công");
				reloadTable();
				return;
			}
			JOptionPane.showMessageDialog(null, "Xóa thất bại");
		} catch (Exception e) {
			JOptionPane.showMessageDialog(null, "Vui lòng chọn một hàng!");
		}
        }//GEN-LAST:event_btnDeleteActionPerformed


