        private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
		try {
			String reason = taReason.getText();
			Employee e = (Employee) cbEmployee.getSelectedItem();
			Boolean reward = rbYes.isSelected();
			Long amount = Long.parseLong(tfAmount.getText());
			System.out.println(e);
			if (EmployeeLogDao.create(new EmployeeLog(e, reward, amount, reason))) {
				JOptionPane.showMessageDialog(null, "Thêm thành công");
				reloadTable();
				return;
			}
			JOptionPane.showMessageDialog(null, "Thêm thất bại");
		} catch (Exception e) {
			JOptionPane.showMessageDialog(null, "Vui lòng nhập thông tin chính xác");
		}
        }//GEN-LAST:event_btnAddActionPerformed

