	private void reloadCbb(Employee e) {
		ecm = new EmployeeComboboxModel(EmployeeDao.findAll());
		if (e != null) {
			ecm.setSelectedItem(e);
		}
		cbEmployee.setModel(ecm);
		cbEmployee.repaint();
	}

