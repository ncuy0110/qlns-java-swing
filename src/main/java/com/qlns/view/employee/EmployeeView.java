        private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
		String firstName, lastName, phoneNumber, address;
		Long salary = 0L;
		try {
			firstName = tfFirstName.getText();
			if (firstName.equals("")) {
				JOptionPane.showMessageDialog(null, "Vui lòng nhập tên");
				return;
			}
			lastName = tfLastName.getText();
			if (lastName.equals("")) {
				JOptionPane.showMessageDialog(null, "Vui lòng nhập họ");
				return;
			}
			phoneNumber = tfPhoneNumber.getText();
			if (phoneNumber.equals("")) {
				JOptionPane.showMessageDialog(null, "Vui lòng nhập số điện thoại");
				return;
			}
			address = tfAddress.getText();
			if (phoneNumber.equals("")) {
				JOptionPane.showMessageDialog(null, "Vui lòng nhập số địa chỉ");
				return;
			}
			salary = Long.parseLong(tfSalary.getText());
			Boolean gender = rbMale.isSelected();
			if (EmployeeDao.create(new Employee(firstName, lastName, phoneNumber, address, salary, gender))) {
				JOptionPane.showMessageDialog(null, "Thêm thành công");
				reloadTable();
				return;
			}

		} catch (HeadlessException | NumberFormatException e) {
			JOptionPane.showMessageDialog(null, "Vui lòng nhập lương hợp lệ");
			return;
		}

		JOptionPane.showMessageDialog(null, "Thêm thất bại");
        }//GEN-LAST:event_btnAddActionPerformed

        private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
		try {
			Employee e = etm.getSelectedRow();
			String firstName, lastName, phoneNumber, address;
			Long salary = 0L;
			firstName = tfFirstName.getText();
			if (firstName.equals("")) {
				JOptionPane.showMessageDialog(null, "Vui lòng nhập tên");
				return;
			}
			lastName = tfLastName.getText();
			if (lastName.equals("")) {
				JOptionPane.showMessageDialog(null, "Vui lòng nhập họ");
				return;
			}
			phoneNumber = tfPhoneNumber.getText();
			if (phoneNumber.equals("")) {
				JOptionPane.showMessageDialog(null, "Vui lòng nhập số điện thoại");
				return;
			}
			address = tfAddress.getText();
			if (phoneNumber.equals("")) {
				JOptionPane.showMessageDialog(null, "Vui lòng nhập số địa chỉ");
				return;
			}

			salary = Long.parseLong(tfSalary.getText());
			e.setFirstName(firstName);
			e.setLastName(lastName);
			e.setAddress(address);
			e.setPhoneNumber(phoneNumber);
			e.setSalary(salary);
			e.setGender(rbMale.isSelected());
			System.out.println(e.getGender());

			if (EmployeeDao.update(e)) {
				JOptionPane.showMessageDialog(null, "Cập nhật thành công");
				reloadTable();
				return;
			}

			JOptionPane.showMessageDialog(null, "Cập nhật thất bại");
		} catch (Exception e) {
			e.printStackTrace();
			JOptionPane.showMessageDialog(null, "Vui lòng chọn một nhân viên");
		}
        }//GEN-LAST:event_btnEditActionPerformed

        private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
		try {
			JTable source = (JTable) evt.getSource();
			int row = source.rowAtPoint(evt.getPoint());
			Employee e = etm.getRow(row);
			etm.setSelectedRow(e);
			lbID.setText("ID: " + e.getId());
			tfFirstName.setText(e.getFirstName());
			tfLastName.setText(e.getLastName());
			tfAddress.setText(e.getAddress());
			tfPhoneNumber.setText(e.getPhoneNumber());
			tfSalary.setText(e.getSalary() + "");
			genderGroup.clearSelection();
			if (e.getGender()) {
				rbMale.setSelected(true);
			} else {
				rbFemale.setSelected(true);
			}
		} catch (Exception e) {
			e.printStackTrace();
		}
        }//GEN-LAST:event_tableMouseClicked

