        private void btnPayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPayActionPerformed
		try {
			Employee e = etm.getSelectedRow();
			System.out.println(e);

			if (SalaryLogDao.isPaid(e.getId())) {
				JOptionPane.showMessageDialog(null, "Đã trả lương tháng này!");
				return;
			}
			List<EmployeeLog> elList = EmployeeLogDao.findInMonthOfEmployee(e.getId());
			Long realSalary = e.getSalary();
			for (EmployeeLog el : elList) {
				if (el.getReward()) {
					realSalary += el.getAmount();
				} else {
					realSalary -= el.getAmount();
				}
			}
			if (SalaryLogDao.create(new SalaryLog(e, realSalary))) {
				JOptionPane.showMessageDialog(null, "Trả hoàn thất");
				reloadTable();
				return;
			}
			JOptionPane.showMessageDialog(null, "Trả thất bại");

		} catch (Exception e) {
			JOptionPane.showMessageDialog(null, "Vui lòng chọn một nhân viên");
		}
        }//GEN-LAST:event_btnPayActionPerformed


